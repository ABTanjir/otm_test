/**
 * Add Class to menu li.
 */
add_filter ( 'nav_menu_css_class', 'add_additional_class_on_li', 10, 4 );
function add_additional_class_on_li($classes, $item, $args) {
    if(isset($args->add_li_class)) {
        $classes[] = $args->add_li_class;
    }
    return $classes;
}


